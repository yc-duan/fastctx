impl Language {
    /// Stable BCP-47-style language code.
    pub(crate) const fn code(self) -> &'static str {
        match self {
            Self::En => "en",
            Self::ZhCn => "zh-CN",
            Self::ZhTw => "zh-TW",
            Self::Ja => "ja",
            Self::Ko => "ko",
            Self::Es => "es",
            Self::Fr => "fr",
            Self::De => "de",
            Self::PtBr => "pt-BR",
            Self::Ru => "ru",
            Self::It => "it",
            Self::Tr => "tr",
            Self::Pl => "pl",
            Self::Nl => "nl",
            Self::Vi => "vi",
            Self::Id => "id",
            Self::Uk => "uk",
        }
    }

    /// Native name shown on the language-selection screen.
    pub(crate) const fn native_name(self) -> &'static str {
        match self {
            Self::En => "English",
            Self::ZhCn => "简体中文",
            Self::ZhTw => "繁體中文",
            Self::Ja => "日本語",
            Self::Ko => "한국어",
            Self::Es => "Español",
            Self::Fr => "Français",
            Self::De => "Deutsch",
            Self::PtBr => "Português (Brasil)",
            Self::Ru => "Русский",
            Self::It => "Italiano",
            Self::Tr => "Türkçe",
            Self::Pl => "Polski",
            Self::Nl => "Nederlands",
            Self::Vi => "Tiếng Việt",
            Self::Id => "Bahasa Indonesia",
            Self::Uk => "Українська",
        }
    }

    /// Parses a supported language code, ignoring case and `_` versus `-` differences.
    pub(crate) fn parse(code: &str) -> Option<Self> {
        let normalized = code.trim().replace('_', "-").to_ascii_lowercase();
        match normalized.as_str() {
            "en" | "en-us" | "en-gb" => Some(Self::En),
            "zh-cn" | "zh-hans" | "zh-sg" => Some(Self::ZhCn),
            "zh-tw" | "zh-hant" | "zh-hk" => Some(Self::ZhTw),
            "ja" | "ja-jp" => Some(Self::Ja),
            "ko" | "ko-kr" => Some(Self::Ko),
            "es" => Some(Self::Es),
            "fr" => Some(Self::Fr),
            "de" => Some(Self::De),
            "pt" | "pt-br" => Some(Self::PtBr),
            "ru" => Some(Self::Ru),
            "it" => Some(Self::It),
            "tr" => Some(Self::Tr),
            "pl" => Some(Self::Pl),
            "nl" => Some(Self::Nl),
            "vi" => Some(Self::Vi),
            "id" => Some(Self::Id),
            "uk" | "ua" => Some(Self::Uk),
            value if value.starts_with("es-") => Some(Self::Es),
            value if value.starts_with("fr-") => Some(Self::Fr),
            value if value.starts_with("de-") => Some(Self::De),
            value if value.starts_with("ru-") => Some(Self::Ru),
            value if value.starts_with("it-") => Some(Self::It),
            value if value.starts_with("tr-") => Some(Self::Tr),
            value if value.starts_with("pl-") => Some(Self::Pl),
            value if value.starts_with("nl-") => Some(Self::Nl),
            value if value.starts_with("vi-") => Some(Self::Vi),
            value if value.starts_with("id-") => Some(Self::Id),
            value if value.starts_with("uk-") => Some(Self::Uk),
            _ => None,
        }
    }

    /// Selects the first-run highlight from the system locale, falling back to English.
    pub(crate) fn detect() -> Self {
        sys_locale::get_locale()
            .as_deref()
            .and_then(Self::parse)
            .unwrap_or(Self::En)
    }

    /// Complete string table for the current language.
    pub(crate) const fn messages(self) -> &'static Messages {
        match self {
            Self::En => &EN,
            Self::ZhCn => &ZH_CN,
            Self::ZhTw => &ZH_TW,
            Self::Ja => &JA,
            Self::Ko => &KO,
            Self::Es => &ES,
            Self::Fr => &FR,
            Self::De => &DE,
            Self::PtBr => &PT_BR,
            Self::Ru => &RU,
            Self::It => &IT,
            Self::Tr => &TR,
            Self::Pl => &PL,
            Self::Nl => &NL,
            Self::Vi => &VI,
            Self::Id => &ID,
            Self::Uk => &UK,
        }
    }
}

